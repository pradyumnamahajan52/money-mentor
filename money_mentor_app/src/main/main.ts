ipcMain.on('ipc-example', async (event, arg) => {
  const msgTemplate = (pingPong: string) => `IPC test: ${pingPong}`;
  console.log(msgTemplate(arg));
  event.reply('ipc-example', msgTemplate('pong'));
});

ipcMain.on('send:checktabledb', async (event, arg) => {
  CheckTableDb();
});

ipcMain.handle('send:authentication', async (event, arg) => {
  const reponse = await new Promise((resolve, reject) => {
    storeAuthentication.set('token', arg.token);
    storeAuthentication.set('user', arg.user);
    resolve('success');
  });
  return reponse;
});

ipcMain.handle('receive:authentication', async (event, arg) => {
  const reponse = await new Promise((resolve, reject) => {
    const token = storeAuthentication.get('token');
    const user = storeAuthentication.get('user');
    resolve({
      token,
      user,
    });
  });
  return reponse;
});

